package smartspace.plugins;

public class StringActionResponse {
	private String action;

	public StringActionResponse() {
		super();
	}
	
	public StringActionResponse(String action) {
		super();
		this.action = action;
	}

	
	public String getAction() {
		return action;
	}

	public void setAction(String action) {
		this.action = action;
	}
	

}
